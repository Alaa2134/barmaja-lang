const { transpile } = require('./transpiler');

const barmajaCode = `
// مثال على دالة ترحب بالمستخدم
دالة ترحيب(اسم) {
    إذا (اسم === غير_معرف || اسم === فارغ) {
        أرجع 'مرحباً أيها الزائر!';
    } وإلا {
        أرجع 'مرحباً يا ' + اسم + '!';
    }
}

دع رسالة = ترحيب('علاء صابر');
اطبع(رسالة);

ثابت س = 10;
ثابت ص = 20;
ثابت مجموع = س + ص;
اطبع('المجموع هو: ' + مجموع);

إذا (مجموع > 15) {
    اطبع('المجموع أكبر من 15');
} وإلا {
    اطبع('المجموع أصغر من أو يساوي 15');
}

لكل (دع i = 0; i < 3; i++) {
    اطبع('تكرار رقم: ' + i);
}
`;

console.log('--- كود بَرمَجة الأصلي ---');
console.log(barmajaCode);

const jsCode = transpile(barmajaCode);

console.log('\n--- كود JavaScript المترجم ---');
console.log(jsCode);

console.log('\n--- تنفيذ الكود المترجم ---');
try {
    eval(jsCode);
} catch (error) {
    console.error('خطأ أثناء التنفيذ:', error);
}
